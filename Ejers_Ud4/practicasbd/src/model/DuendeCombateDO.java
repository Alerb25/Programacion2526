public class DuendeCombateDO {

    private int idDuende;
    private String nombre;
    private String apodoGuerra;
    private String modelo;
    private int agilidad;
    private int nivelSarcasmo;
    private double horasSombra;
    private double tiempoBrilloCegador;

    // Getters y Setters
    public int getIdDuende() { return idDuende; }
    public void setIdDuende(int idDuende) { this.idDuende = idDuende; }

    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }

    public String getApodoGuerra() { return apodoGuerra; }
    public void setApodoGuerra(String apodoGuerra) { this.apodoGuerra = apodoGuerra; }

    public String getModelo() { return modelo; }
    public void setModelo(String modelo) { this.modelo = modelo; }

    public int getAgilidad() { return agilidad; }
    public void setAgilidad(int agilidad) { this.agilidad = agilidad; }

    public int getNivelSarcasmo() { return nivelSarcasmo; }
    public void setNivelSarcasmo(int nivelSarcasmo) { this.nivelSarcasmo = nivelSarcasmo; }

    public double getHorasSombra() { return horasSombra; }
    public void setHorasSombra(double horasSombra) { this.horasSombra = horasSombra; }

    public double getTiempoBrilloCegador() { return tiempoBrilloCegador; }
    public void setTiempoBrilloCegador(double tiempoBrilloCegador) { this.tiempoBrilloCegador = tiempoBrilloCegador; }
}