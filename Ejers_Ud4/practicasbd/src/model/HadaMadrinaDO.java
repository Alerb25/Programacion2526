public class HadaMadrinaDO {

    private int idHada;
    private String nombre;
    private String apodoGuerra;
    private int edad;
    private double envergaduraAlas;
    private int nivelPasivoAgresivo;
    private double polvoPurpurina;

    public int getIdHada() { return idHada; }
    public void setIdHada(int idHada) { this.idHada = idHada; }

    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }

    public String getApodoGuerra() { return apodoGuerra; }
    public void setApodoGuerra(String apodoGuerra) { this.apodoGuerra = apodoGuerra; }

    public int getEdad() { return edad; }
    public void setEdad(int edad) { this.edad = edad; }

    public double getEnvergaduraAlas() { return envergaduraAlas; }
    public void setEnvergaduraAlas(double envergaduraAlas) { this.envergaduraAlas = envergaduraAlas; }

    public int getNivelPasivoAgresivo() { return nivelPasivoAgresivo; }
    public void setNivelPasivoAgresivo(int nivelPasivoAgresivo) { this.nivelPasivoAgresivo = nivelPasivoAgresivo; }

    public double getPolvoPurpurina() { return polvoPurpurina; }
    public void setPolvoPurpurina(double polvoPurpurina) { this.polvoPurpurina = polvoPurpurina; }
}