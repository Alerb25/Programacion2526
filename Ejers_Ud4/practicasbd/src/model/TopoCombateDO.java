public class TopoCombateDO {

    private int idTopo;
    private String nombre;
    private String apodoGuerra;
    private int edad;
    private String modelo;
    private double fuerzaExcavacion;
    private double agudezaOlfativa;
    private int horasSuenno;
    private int tiempoTierraEnOjos;

    public int getIdTopo() { return idTopo; }
    public void setIdTopo(int idTopo) { this.idTopo = idTopo; }

    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }

    public String getApodoGuerra() { return apodoGuerra; }
    public void setApodoGuerra(String apodoGuerra) { this.apodoGuerra = apodoGuerra; }

    public int getEdad() { return edad; }
    public void setEdad(int edad) { this.edad = edad; }

    public String getModelo() { return modelo; }
    public void setModelo(String modelo) { this.modelo = modelo; }

    public double getFuerzaExcavacion() { return fuerzaExcavacion; }
    public void setFuerzaExcavacion(double fuerzaExcavacion) { this.fuerzaExcavacion = fuerzaExcavacion; }

    public double getAgudezaOlfativa() { return agudezaOlfativa; }
    public void setAgudezaOlfativa(double agudezaOlfativa) { this.agudezaOlfativa = agudezaOlfativa; }

    public int getHorasSuenno() { return horasSuenno; }
    public void setHorasSuenno(int horasSuenno) { this.horasSuenno = horasSuenno; }

    public int getTiempoTierraEnOjos() { return tiempoTierraEnOjos; }
    public void setTiempoTierraEnOjos(int tiempoTierraEnOjos) { this.tiempoTierraEnOjos = tiempoTierraEnOjos; }
}